#include "../minishell.h"

void	*ft_realloc(void *ptr, size_t new_size, size_t old_count)
{
	void	*new_ptr;

	if (!ptr)
		return (malloc(new_size));
	new_ptr = malloc(new_size);
	if (!new_ptr)
		return (NULL);
	ft_memcpy(new_ptr, ptr, old_count);
	free(ptr);
	return (new_ptr);
}
