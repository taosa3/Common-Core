#include "push_swap.h"

int	stack_len(t_stack_node *stack)
{
	int	count;

	count = 0;
	while (stack)
	{
		count++;
		stack = stack->next;
	}
	return (count);
}

int	check_valid_numbers(char *argv)
{
	int	i;

	i = 0;
	if (argv[i] == '-' || argv[i] == '+')
		i++;
	while (argv[i])
	{
		if (!ft_isdigit(argv[i]))
			return (0);
		i++;
	}
	return (1);
}

int	check_duplicates(t_stack_node *stack, int n)
{
	while (stack)
	{
		if (stack->n == n)
			return (0);
		stack = stack->next;
	}
	return (1);
}
